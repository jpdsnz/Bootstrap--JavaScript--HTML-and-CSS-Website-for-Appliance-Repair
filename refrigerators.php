          <h3 class="SecondHeading">Old vs. Modern Refrigeration</h3>
          <p>Older refrigeration models utilized freon gas known as CFC (which was later found to hurt the environment) as opposed modern HFC gas. However, process of refrigeration remains quite similar despite the change in chemicals.</p>
        </div>
        <div class="col-sm-12 text-center col-md-12 col-lg-6">
          <h3 class="SecondHeading">How old refrigerators work?</h3>
          <p>If you look at the back or bottom of an older refrigerator, you'll see a long thin tube that loops back and forth. This tube is connected to a pump, which is powered by an electric motor. Inside the tube is Freon (CFC gas.) CFC starts out as a liquid. The pump pushes the CFC through a lot of coils in the freezer area. There the chemical turns to a vapor. When it does, it soaks up some of the heat that may be in the freezer compartment. As it does this, the coils get colder and the freezer begins to get colder. In the regular part of your refrigerator, there are fewer coils and a larger space. So, less heat is soaked up by the coils and the CFC vapor. The pump then sucks the CFC as a vapor and forces it through thinner pipes which are on the outside of the refrigerator. By compressing it, the CFC turns back into a liquid and heat is given off and is absorbed by the air around it. That's why it might be a little warmer behind or under your refrigerator. Once the CFC passes through the outside coils, the liquid is ready to go back through the freezer and refrigerator continually. <br>
          </p>
</div>
        <div class="col-sm-12 text-center col-md-12 col-lg-6">
          <h3 class="SecondHeading">How modern refrigerators work?</h3>
          <p>Modern refrigerators utilize HFC-134a gas which turns into a liquid qhen cooled to -15.9 degrees.&nbsp;A motor and compressor squeezes the HFC. When it is compressed, a gas heats up as it is pressurized. When you pass the compressed gas through the coils on the back or bottom of a modern refrigerator, the warmer gas can lose its heat to the air in the room.&nbsp;As it cools, the HFC can change into a liquid because it is under a high pressure. The liquid flows through what's called an expansion valve, a tiny small hole that the liquid has to squeeze through. Between the valve and the compressor, there is a low-pressure area because the compressor is pulling the ammonia gas out of that side. When the liquid HFC hits a low pressure area it boils and changes into a gas. This is called vaporizing. The coils then go through the freezer and regular part of the refrigerator where the colder liquid in the coil pulls the heat out of the compartments. This makes the inside of the freezer and entire refrigerator cold. The compressor sucks up the cold gas, and the gas goes back through the same process cycle.</p>
        </div>
