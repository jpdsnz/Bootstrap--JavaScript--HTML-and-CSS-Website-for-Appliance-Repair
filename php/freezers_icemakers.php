        <div class="col-sm-12 text-center col-md-12 col-lg-6">
          <h3 class="SecondHeading">How ice machines work?</h3>
          <p>Ice machines also work with the same refrigrant type systems. These machines utilize refrigerant by pressurizing gas and then condensing it. This is taken into the evaporator which has a constant running water line running over its surface. Water is cooled down to freezing and refrigerant is then sent back into the evaporator to loosen the newly formed cubes. </p>
        </div>
        <div class="col-sm-12 text-center col-md-12 col-lg-6">
          <h3 class="SecondHeading">Freezer tips!</h3>
          <ul class="Tips">
            <li>Clear/remove anything blocking the air flow around the outside of the freezer.&nbsp;Allow 3" of clearance all the way around.</li>
            <li>If there are condensor coils on the rear of the box, make sure the box is several inches from the wall and that nothing is on top at the rear</li>
            <li>If the condenser coils are underneath the refrigerator, clean the coils at least twice a year. </li>
            
          	
          	</ul>
        </div>
        <div class="col-sm-12 text-center col-md-12 col-lg-6">
          <h3 class="SecondHeading">Ice Machine tips!</h3>
<ul class="Tips">
		    <li>A charcoal water filter on the incoming water line improves the taste and odor of the water used to make the ice.</li>
			  <li>Replace the water filter on the water supply line every six months. </li>
			  <li>Replace air filter at least twice per year, if applicable. </li>
			  <li>Follow carefully manufacturer instructions for cleaning the water reservoir every two to three months. Mineral scale buildup is a health concern. </li>
			  <li>Sanitize cube bin every two to three months.</li>
