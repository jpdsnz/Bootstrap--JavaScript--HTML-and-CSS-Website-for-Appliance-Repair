          <h3 class="SecondHeading">How freezers work?</h3>
          <p>A freezer works in a similar fashion as the refrigerator and utilizes the same systems to cycle compressing gas refrigerant into liquid and back again in a continuous process. This process maintains temperatures below freezing point of water. This helps to store perishable food for longer periods of time than a refrigerator. <br>
          </p>
